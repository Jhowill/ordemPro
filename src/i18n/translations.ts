function getValue(tree: TranslationTree | string | undefined, path: string[]): string | undefined {
  if (!tree || typeof tree === 'string') return typeof tree === 'string' ? tree : undefined;
  const [head, ...rest] = path;
  const next = tree[head];
  if (rest.length === 0) return typeof next === 'string' ? next : undefined;
  return getValue(next, rest);
}

function interpolate(template: string, params?: Record<string, string | number>) {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ''));
}

export function createTranslator(locale: AppLocale) {
  return (key: string, params?: Record<string, string | number>) => {
    const path = key.split('.');
    const value =
      getValue(translations[locale], path) ??
      getValue(translations.pt, path) ??
      key;
    return interpolate(value, params);
  };
}

export function getLocaleName(locale: AppLocale, currentLocale: AppLocale) {
  return createTranslator(currentLocale)(`locale.${locale}`);
}

export function getStatusLabel(status: ServiceOrderStatus, locale: AppLocale) {
  return createTranslator(locale)(`status.${status}`);
}

export function getLocaleIntl(locale: AppLocale) {
  return locale === 'pt' ? 'pt-BR' : locale === 'en' ? 'en-US' : locale === 'fr' ? 'fr-FR' : 'es-ES';
}

